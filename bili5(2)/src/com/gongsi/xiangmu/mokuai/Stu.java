package com.gongsi.xiangmu.mokuai;

public class Stu {
	private String name;
	private int age;

	public Stu(String name, int age) {
		// TODO 自动生成的构造函数存根
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
}
