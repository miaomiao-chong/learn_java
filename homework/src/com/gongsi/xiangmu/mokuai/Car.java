package com.gongsi.xiangmu.mokuai;

public class Car {
	private String id;
	private double l,t,r;//l:里程  t:时间 r:效率
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public double getL() {
		return l;
	}
	public void setL(double l) {
		this.l = l;
	}
	public double getT() {
		return t;
	}
	public void setT(double t) {
		this.t = t;
	}
	public double getR() {
		return r;
	}
	public void setR(double r) {
		this.r = r;
	}
	

	
	
}
